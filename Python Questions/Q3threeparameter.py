x = input("Enter the first number: ")
y = input("Enter the second number: ")
z = input("Enter a mathematical operator: ")
if z == "+":
    a = int(x)+int(y)
    print("result", a)
elif z == "-":
    a = int(x)-int(y)
    print("result",a)
elif z == "*":
    a = int(x)*int(y)
    print("result",a)
elif z == "/":
    b = int(x)/int(y)
    print("result",b)
       





